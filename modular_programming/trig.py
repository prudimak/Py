import math
def get_sine(x):
    angle_rad=math.radians(x)
    angle_sin=math.sin(angle_rad)
    return angle_sin
def get_cos(x):
    angle_rad=math.radians(x)
    angle_cos=math.cos(angle_rad)
    return angle_cos
def get_tan(x):
    angle_rad=math.radians(x)
    angle_tan=math.tan(angle_rad)
    return angle_tan

